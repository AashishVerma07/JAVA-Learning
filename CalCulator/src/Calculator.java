public class Calculator {
    public float addition(float a, float b){
        float c = a + b;
        return c;
    }

    public float subsitution(float a, float b){
        float c = a - b;
        return c;
    }

    public float multiply(float a, float b){
        float c = a * b;
        return c;
    }

    public float devide(float a, float b){
        float c = a / b;
        return c;
    }
}
